public class CWH_Ch2_Operators {
    public static void main(String[] args) {
        int a = 4;//Arithmetic Operaters
        int b = 6 % a ; // Modular Operators
        System.out.println(b);

        int c = 9; //Assignment Operaters
        c *= 3;
        System.out.println(c);

       System.out.println(64<6);// Comparison Oprators

       System.out.println(64>5 && 64>8);//Logical Operators
       System.out.println(64>5 || 64>98);//Logical Operators
       System.out.println(2&3);// Bitwise Operators





    }
}
